package net04_xml;

public class Family {
	private String family;
	private String father;
	private String mother;
	private String me;

	protected String getFamily() {
		return family;
	}
	protected void setFamily(String family) {
		this.family = family;
	}
	protected String getFather() {
		return father;
	}
	protected void setFather(String father) {
		this.father = father;
	}
	
	protected String getMother() {
		return mother;
	}
	
	protected void setMother(String mother) {
		this.mother = mother;
	}
	
	protected String getMe() {
		return me;
	}
	
	protected void setMe(String me) {
		this.me = me;
	}
	
	
 

}
