package dataStack;

public class StackVO {
	

	/**
	 * 마지막 위치값
	 */
	private int top;
	/**
	 * 전체길이
	 */
	private int pos;
	
	StackVO() {
		pos = 0;
		top = 0;
	}
	
	
	
	public int getTop() {
		return top;
	}
	public void setTop(int top) {
		this.top = top;
	}
	public int getPos() {
		return pos;
	}
	public void setPos(int pos) {
		this.pos = pos;
	}
	


	
	
	
	

}
