package ch22_collection02.clazz.exam;

public class Board {
	private String name;
	private String alias;
	private String addr;
	private String dangdo;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAlias() {
		return alias;
	}
	public void setAlias(String alias) {
		this.alias = alias;
	}
	public String getAddr() {
		return addr;
	}
	public void setAddr(String addr) {
		this.addr = addr;
	}
	public String getDangdo() {
		return dangdo;
	}
	public void setDangdo(String dangdo) {
		this.dangdo = dangdo;
	}

}
