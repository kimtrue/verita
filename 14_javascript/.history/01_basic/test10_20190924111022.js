/**
 * 데이터 타입에 대한 검사
 * (자료형 검사)
 * 
 * typeof 검사할 데이터 -> 문자열로 타입을 반환한다
 * typeof (데이터)
 * "number", "string", ...
 * 
 * null데이터 타입은 "null"이 아니라 object로 반환된다
 * 
 */

