package com.bittago.repository.vo;

public class Nonuser {

	private int nonuserNo;			// 비회원 번호
	private String nonuserName;		// 비회원 이름
	private String nonuserPhone;	// 비회원 휴대전화번호
	private String nonuserEmail;	// 비회원 이메일
	public int getNonuserNo() {
		return nonuserNo;
	}
	public void setNonuserNo(int nonuserNo) {
		this.nonuserNo = nonuserNo;
	}
	public String getNonuserName() {
		return nonuserName;
	}
	public void setNonuserName(String nonuserName) {
		this.nonuserName = nonuserName;
	}
	public String getNonuserPhone() {
		return nonuserPhone;
	}
	public void setNonuserPhone(String nonuserPhone) {
		this.nonuserPhone = nonuserPhone;
	}
	public String getNonuserEmail() {
		return nonuserEmail;
	}
	public void setNonuserEmail(String nonuserEmail) {
		this.nonuserEmail = nonuserEmail;
	}
	
}
